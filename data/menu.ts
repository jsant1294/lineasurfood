// Lightweight inline SVG data-URI photos so the demo looks complete without
// external assets. Vendors replace these in the admin with real photos.
function dish(emoji: string, c1: string, c2: string): string {
  const svg = `<svg xmlns='http://www.w3.org/2000/svg' width='400' height='400'><defs><radialGradient id='g' cx='50%' cy='38%' r='75%'><stop offset='0%' stop-color='${c1}'/><stop offset='100%' stop-color='${c2}'/></radialGradient></defs><rect width='400' height='400' fill='url(#g)'/><text x='50%' y='53%' font-size='180' text-anchor='middle' dominant-baseline='middle'>${emoji}</text></svg>`;
  return `data:image/svg+xml;utf8,${encodeURIComponent(svg)}`;
}

